#include <stdio.h>
/**
 * add - Add 2 Integers.
 * @a: 1st Int.
 * @b: 2nd Int.
 * Return: Addition.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Subtract 2 Integers.
 * @a: 1st Int.
 * @b: 2nd Int.
 * Return: Subtraction.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * Mod - Mod 2 Integers.
 * @a: 1st Int.
 * @b: 2nd Int.
 * Return: Result.
 */
int mod(int a, int b)
{
	return (a %  b);
}

/**
 * mul - Multiply 2 Integers.
 * @a: 1st Int.
 * @b: 2nd Int.
 * Return: Multiplication.
 */
int mul(int a, int b)
{
	return (a *  b);
}

/**
 * div - Divide 2 Integers.
 * @a: 1st Int.
 * @b: 2nd Int.
 * Return: Division.
 */
int div(int a, int b)
{
	return (a %  b);
}
