#include "main.h"
/**
 *print_alphabet_x10 - make ten times the alphabet, in lowercase
 *Return: Always 0
 */
void print_alphabet_x10(void)
{
/*print_alphabet_x10(void) is a fucntion prototype*/
char c;
int i = 0;
while (i <= 0)
{
for (c = 'a'; c <= 'z'; c++)
{
_putchar(c);
}
_putchar('\n');
i++;
}
}
